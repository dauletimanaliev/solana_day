const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const rateLimit = require('express-rate-limit');
const winston = require('winston');

// Ð˜Ð¼Ð¿Ð¾Ñ€Ñ‚ Ñ€Ð¾ÑƒÑ‚Ð¾Ð²
const assetsRoutes = require('./routes/assets');
const transactionsRoutes = require('./routes/transactions');

// ÐÐ°ÑÑ‚Ñ€Ð¾Ð¹ÐºÐ° Ð»Ð¾Ð³Ð¸Ñ€Ð¾Ð²Ð°Ð½Ð¸Ñ
const logger = winston.createLogger({
  level: 'info',
  format: winston.format.combine(
    winston.format.timestamp(),
    winston.format.errors({ stack: true }),
    winston.format.json()
  ),
  transports: [
    new winston.transports.File({ filename: 'logs/error.log', level: 'error' }),
    new winston.transports.File({ filename: 'logs/combined.log' })
  ]
});

if (process.env.NODE_ENV !== 'production') {
  logger.add(new winston.transports.Console({
    format: winston.format.simple()
  }));
}

const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(helmet());
app.use(cors());
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true }));

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 Ð¼Ð¸Ð½ÑƒÑ‚
  max: 100 // Ð¼Ð°ÐºÑÐ¸Ð¼ÑƒÐ¼ 100 Ð·Ð°Ð¿Ñ€Ð¾ÑÐ¾Ð² Ñ Ð¾Ð´Ð½Ð¾Ð³Ð¾ IP
});
app.use(limiter);

// Ð›Ð¾Ð³Ð¸Ñ€Ð¾Ð²Ð°Ð½Ð¸Ðµ Ð·Ð°Ð¿Ñ€Ð¾ÑÐ¾Ð²
app.use((req, res, next) => {
  logger.info(`${req.method} ${req.path}`, {
    ip: req.ip,
    userAgent: req.get('User-Agent')
  });
  next();
});

// Health check
app.get('/health', (req, res) => {
  res.json({
    status: 'healthy',
    timestamp: new Date().toISOString(),
    service: 'Asset Tokenization API',
    version: '1.0.0'
  });
});

// API Ñ€Ð¾ÑƒÑ‚Ñ‹
app.use('/api/assets', assetsRoutes);
app.use('/api/transactions', transactionsRoutes);

// ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚ÐºÐ° Ð¾ÑˆÐ¸Ð±Ð¾Ðº
app.use((err, req, res, next) => {
  logger.error('Unhandled error:', err);
  res.status(500).json({
    success: false,
    error: 'Internal server error'
  });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({
    success: false,
    error: 'Endpoint not found'
  });
});

// Ð—Ð°Ð¿ÑƒÑÐº ÑÐµÑ€Ð²ÐµÑ€Ð°
app.listen(PORT, () => {
  logger.info(`ðŸš€ Integrated Asset Tokenization API Ð·Ð°Ð¿ÑƒÑ‰ÐµÐ½ Ð½Ð° Ð¿Ð¾Ñ€Ñ‚Ñƒ ${PORT}`);
  logger.info(`ðŸ“‹ Health check: http://localhost:${PORT}/health`);
  logger.info(`ðŸ”— API endpoints:`);
  logger.info(`   POST /api/assets/create_asset`);
  logger.info(`   POST /api/assets/mint_fraction`);
  logger.info(`   GET  /api/assets/:assetId`);
  logger.info(`   GET  /api/assets`);
  logger.info(`   POST /api/transactions/buy_fraction`);
  logger.info(`   POST /api/transactions/distribute_revenue`);
});

module.exports = app;
